// Расширенный банк событий экспедиции (≈730 сценариев), собирается генератором
// из строковых банок. Голос — тот же лягушка-пилот в бортовом дневнике.
// Токены: {galaxy}/{arm}/{planet}/{star}/{anomaly}/{phenomenon}/{creature}/
// {faction}/{place}/{adj}ое — роллятся из DICT; {gold}/{slime}/{mutagen} — лут.
// Лут-сценарии помечены set:['loot'] и несут loot:{...} → попадают в фильтр
// каталога по награде.
import type { Scenario } from './types'

type Tag = 'weird' | 'awe' | 'spooked' | 'lonely' | 'loot' | 'combat' | 'wreck'

let _seq = 0
const sid = (cat: string) => `x_${cat}_${(_seq++).toString(36)}`

interface Spec {
  w?: number
  min?: number
  set?: Tag[]
  needs?: Tag
}

// Одно-строчный сценарий.
function one(
  category: Scenario['category'],
  text: string,
  spec: Spec = {},
): Scenario {
  const s: Scenario = {
    id: sid(category),
    category,
    weight: spec.w ?? 3,
    lines: [{ dt: 0, text }],
  }
  if (spec.min) s.minSec = spec.min
  if (spec.set) s.set = spec.set
  if (spec.needs) s.needs = spec.needs
  return s
}

// Двух-строчный сценарий (вторая строка через ~4с).
function two(
  category: Scenario['category'],
  a: string,
  b: string,
  spec: Spec = {},
): Scenario {
  const s: Scenario = {
    id: sid(category),
    category,
    weight: spec.w ?? 3,
    lines: [
      { dt: 0, text: a },
      { dt: 4, text: b },
    ],
  }
  if (spec.min) s.minSec = spec.min
  if (spec.set) s.set = spec.set
  if (spec.needs) s.needs = spec.needs
  return s
}

